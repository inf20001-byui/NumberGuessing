//Define Colors for use in my software

public class Color {
    public static final String RESET = "\033[0m";  // Text Reset
    public static final String RED = "\033[0;31m";     // RED
    public static final String GREEN = "\033[0;32m";   // GREEN
    public static final String YELLOW = "\033[0;33m";  // YELLOW
    public static final String BLUE = "\033[0;34m";    // BLUE
    public static final String PURPLE = "\033[0;35m";  // PURPLE
    public static final String CYAN = "\033[0;36m";    // CYAN
    public static final String WHITE = "\033[0;37m";   // WHITE
    public static final String ANSI_BLACK_BACKGROUND = "\u001B[40m"; // BLACK "normal"  Background
    public static final String ANSI_RED_BACKGROUND = "\u001B[41m"; // RED Background
    public static final String ANSI_GREEN_BACKGROUND = "\u001B[42m"; // GREEN Background
    public static final String ANSI_YELLOW_BACKGROUND = "\u001B[43m"; // YELLOW Background
    public static final String ANSI_BLUE_BACKGROUND = "\u001B[44m"; // BLUE Background
    public static final String ANSI_PURPLE_BACKGROUND = "\u001B[45m"; // PURPLE Background
    public static final String ANSI_CYAN_BACKGROUND = "\u001B[46m"; // CYAN Background
    public static final String ANSI_WHITE_BACKGROUND = "\u001B[47m"; // WHITE Background
    }
